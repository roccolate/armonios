static int glyph_index(char ch) {
    if (ch >= '0' && ch <= '9') {
        return ch - '0';
    }

    if (ch >= 'a' && ch <= 'z') {
        ch = (char)(ch - ('a' - 'A'));
    }

    if (ch >= 'A' && ch <= 'Z') {
        return 10 + ch - 'A';
    }

    return -1;
}

void font_draw_char(fb_t *fb, uint32_t x, uint32_t y, char ch,
                    uint32_t color) {
    int index = glyph_index(ch);

    if (fb == 0 || fb->pixels == 0 || index < 0) {
        return;
    }

    for (uint32_t row = 0; row < FONT_GLYPH_HEIGHT; row++) {
        uint8_t bits = g_font_glyphs[index][row];

        for (uint32_t col = 0; col < FONT_GLYPH_WIDTH; col++) {
            uint8_t mask = (uint8_t)(1U << (FONT_GLYPH_WIDTH - 1U - col));

            if ((bits & mask) != 0) {
                fb_putpixel(fb, x + col, y + row, color);
            }
        }
    }
}

void font_draw_text(fb_t *fb, uint32_t x, uint32_t y, const char *text,
                    uint32_t color) {
    uint32_t cursor_x = x;
    uint32_t cursor_y = y;

    if (fb == 0 || text == 0) {
        return;
    }

    while (*text != '\0') {
        if (*text == '\n') {
            cursor_x = x;
            cursor_y += FONT_LINE_HEIGHT;
        } else if (*text == ' ') {
            cursor_x += FONT_CHAR_WIDTH;
        } else {
            font_draw_char(fb, cursor_x, cursor_y, *text, color);
            cursor_x += FONT_CHAR_WIDTH;
        }
        text++;
    }
}
